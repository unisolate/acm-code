#include <cstdio>
#include <cstring>
#define MOD 1000000007
using namespace std;
char x[100], y[100], z[100];
bool isdigit(char c)
{
    if (c >= '0' && c <= '9')
        return true;
    else return false;
}
int run(int p, int add)
{
    if (p < 0)
    {
        if (add > 0)
            return 0;
        else return 1;
    }
    int ans = 0, q[50];
    memset(q, 0, sizeof(q));
    for (int i = 0; i < 10; ++i)
    {
        if (p == 0 && i == 0)
            continue;
        if (isdigit(x[p]) && (x[p] - '0') != i)
            continue;
        for (int j = 0; j < 10; ++j)
        {
            if (p == 0 && j == 0)
                continue;
            if (isdigit(y[p]) && (y[p] - '0') != j)
                continue;
            for (int k = 0; k < 10; ++k)
            {
                if (p == 0 && k == 0)
                    continue;
                if (isdigit(z[p]) && (z[p] - '0') != k)
                    continue;
                if ((i + j + add) % 10 != k)
                    continue;
                q[(i + j + add) / 10]++;
            }
        }
    }
    for (int i = 0; i < 50; ++i)
    {
        if (!q[i])
            continue;
        if (p == 0)
        {
            ans += (q[0]) % MOD;
            break;
        }
        printf("%d %d\n", p - 1, i);
        ans += (q[i] * run(p - 1, i)) % MOD;
    }
    return ans % MOD;
}
int main()
{
    while (~scanf("%s", x) && x[0] != '0')
    {
        scanf("%s%s", y, z);
        int len = strlen(x);
        printf("%d\n", run(len - 1, 0));
    }
    return 0;
}
